def findLeftMost(arr):
    arr.insert(0,'c')
    ans = arr[27]
    calcBlock = arr[:27]
    calcBlock = calcBlock[::-1]
    mid = calcBlock[0] ^ calcBlock[23] ^ calcBlock[24]
    arr[0] = mid ^ ans


